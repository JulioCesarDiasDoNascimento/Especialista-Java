package desafios.V_XXXV;

public class GeometriaUtil {
    static double pi = 3.14159265358979323846;

    static double perimetroQuadrado(double areaQuadrado){
        return areaQuadrado * areaQuadrado;

    }

    static double perimetroCirculo(double areaCirculo){
        return areaCirculo * (areaCirculo * pi);

    }
}
